namespace Waldhacker\Pseudify\Core\Processor\Processing;

class DataProcessing implements DataProcessingInterface
{
    private readonly string $identifier;

    /**
     * @api
     */
    public function __construct(
        private readonly \Closure $processor,
        ?string $identifier = null,
    ) {
        $this->identifier = $identifier ?? 'autogenerated-'.uniqid();
    }

    /**
     * @internal
     */
    #[\Override]
    public function getIdentifier(): string
    {
        return $this->identifier;
    }

    /**
     * @internal
     */
    #[\Override]
    public function getProcessor(): callable
    {
        return $this->processor;
    }
}
